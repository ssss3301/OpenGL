void MultiLightSource::load_textures() {
	int width, height, channel;
	unsigned char* diffuse_data = stbi_load("textures/container2.png", &width, &height, &channel, 0);
	if (diffuse_data) {
		glGenTextures(1, &_diffuseTex);
		glBindTexture(GL_TEXTURE_2D, _diffuseTex);
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, diffuse_data);
		glGenerateMipmap(GL_TEXTURE_2D);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		stbi_image_free(diffuse_data);
		glBindTexture(GL_TEXTURE_2D, 0);
	}

	unsigned char* specular_data = stbi_load("textures/container2_specular.png", &width, &height, &channel, 0);
	if (specular_data) {
		glGenTextures(1, &_specularTex);
		glBindTexture(GL_TEXTURE_2D, _specularTex);
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, specular_data);
		glGenerateMipmap(GL_TEXTURE_2D);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		stbi_image_free(specular_data);
		glBindTexture(GL_TEXTURE_2D, 0);
	}

}
