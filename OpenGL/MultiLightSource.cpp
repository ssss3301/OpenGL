#include "MultiLightSource.h"
#include "stb_image.h"


MultiLightSource::MultiLightSource() : _cam(glm::vec3(0.0f, 0.0f, 3.0f)){
	_vao = 0;
	_vbo = 0;
	_diffuseTex = 0;
	_specularTex = 0;
}

bool MultiLightSource::init(int width, int height, std::string& title, GLFWmonitor* monitor, GLFWwindow* share) {
	if (!GLApplication::init(width, height, title, monitor, share)) {
		return false;
	}
	_shaderProg.init_with_shader_file("shaders/multilightsource.vert", "shaders/multilightsource.frag");
	setup_vao();
	load_textures();
	return true;
}

